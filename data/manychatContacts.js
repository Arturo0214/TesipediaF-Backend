export const ALL_SEGMENTS = {
  SUPER_HOT,
  HOT,
  WARM,
  TIBIO_1,
  TIBIO_2,
};

/**
 * Obtiene la lista plana de todos los contactos con su segmento
 * Filtra excluidos y admins automáticamente
 */
export function getAllContacts() {
  const allExcluded = new Set([...EXCLUDE_PHONES, ...ADMIN_PHONES]);
  const contacts = [];

  for (const [segment, list] of Object.entries(ALL_SEGMENTS)) {
    for (const c of list) {
      if (!allExcluded.has(c.p)) {
        contacts.push({ wa_id: c.p, nombre: c.n || '', segment });
      }
    }
  }

  return contacts;
}

/**
 * Obtiene contactos de un segmento específico
 */
export function getContactsBySegment(segmentName) {
  const allExcluded = new Set([...EXCLUDE_PHONES, ...ADMIN_PHONES]);
  const list = ALL_SEGMENTS[segmentName] || [];
  return list
    .filter(c => !allExcluded.has(c.p))
    .map(c => ({ wa_id: c.p, nombre: c.n || '', segment: segmentName }));
}

