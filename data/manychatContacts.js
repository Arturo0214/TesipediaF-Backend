export const ALL_SEGMENTS = {
  SUPER_HOT,
  HOT,
  WARM,
  TIBIO_1,
  TIBIO_2,
  FRIO,
  NEVER,
};

/**
 * Obtiene la lista plana de todos los contactos con su segmento
 * Filtra excluidos y admins automáticamente
 */
export function getAllContacts() {
  const allExcluded = new Set([...EXCLUDE_PHONES, ...ADMIN_PHONES]);
  const seen = new Map(); // wa_id -> contact (keeps first occurrence = highest priority)

  // Iterate in priority order so first occurrence wins
  for (const segment of SEGMENT_PRIORITY) {
    const list = ALL_SEGMENTS[segment] || [];
    for (const c of list) {
      if (!allExcluded.has(c.p) && !seen.has(c.p)) {
        seen.set(c.p, { wa_id: c.p, nombre: c.n || '', segment });
      }
    }
  }

  return Array.from(seen.values());
}

/**
 * Obtiene contactos de un segmento específico
 */
export function getContactsBySegment(segmentName) {
  const allExcluded = new Set([...EXCLUDE_PHONES, ...ADMIN_PHONES]);
  const list = ALL_SEGMENTS[segmentName] || [];
  return list
    .filter(c => !allExcluded.has(c.p))
    .map(c => ({ wa_id: c.p, nombre: c.n || '', segment: segmentName }));
}

export const SEGMENT_PRIORITY = ['SUPER_HOT', 'HOT', 'WARM', 'TIBIO_1', 'TIBIO_2', 'FRIO', 'NEVER'];
